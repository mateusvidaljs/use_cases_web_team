public class PlanoOperadoraIf {
  public static void main(String[] args) {

    String plano = "M";

    if (plano == "B") {
      System.out.println("100 minutos de ligacao");

    } else if (plano == "M") {
      System.out.println("100 minutos de ligacao");
      System.out.println("WhatsApp e Instagram gratis");

    } else if (plano == "T") {
      System.out.println("100 minutos de ligacao");
      System.out.println("WhatsApp e Instagram gratis");
      System.out.println("5Gb Youtube");
    }
  }
}
